package com.oops.interfaceclass;

// Interface
//      An interface in Java is a group of abstract methods used to achieve a unique abstraction in object oriented programming.,
//      used to achieve abstraction and multiple inheritance

// Key Features of Interface
//      ✔ 100% abstraction (traditionally)
//      ✔ Supports multiple inheritance
//      ✔ Cannot create objects
//      ✔ Methods are public by default
//      ✔ Variables are public static final

public interface Engine {

    void start();
    void accelerator();
    void stop();

}
